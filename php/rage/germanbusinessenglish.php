	<h2>German Business English</h2>


	<p>
				As I argued with a German consultant over the word “bi-weekly”
				it became clear to me that German Business English is a dialect
				with its own conventions and standards, as different from
				British English as Australian, say.</p><p>In this case, the
				consultant was not prepared to put the word “fortnightly” into
				his document as it was not a word with which he was familiar. I
				explained that “bi-weekly” is ambiguous - it can mean “twice a
				week” just as easily as it can mean “every two weeks”. However
				its usage in German Business English appears to be entrenched.</p>
				<p>I’m not saying this is a bad thing. Living dialects are part
