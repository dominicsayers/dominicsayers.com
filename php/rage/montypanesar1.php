	<h2>Mudhsuden Singh Panesar</h2>

	<p>
				Monty Panesar’s fielding was memorably described as
				“<a target="_blank" href="http://www.telegraph.co.uk/sport/main.jhtml?xml=/sport/2006/05/30/scprin30.xml">slapstick</a>”
				by Derek Pringle in the Telegraph today. He seems prone to
				fumbles and spilled chances (Panesar as well, boom boom).
				Anybody who has played cricket knows a talentless fielder when
				they see one. When Kevin Pietersen dropped his first six chances
				in Test cricket it was clearly a blip, one that he has since
				rectified by catching everything that has come to him.<p>No such
				expectations accompany Panesar’s fielding which will remain for
				the remainder of his career a weak part of his game. Some
				commentators are warning that his fielding is so weak that the
				remainder of his career may not stretch to the Ashes campaign
				this winter. It’s my opinion that this would be an enormous
				error on the part of the selectors, if indeed they are
				contemplating dropping him.</p>
				<p>Monty’s fielding will doubtless improve. He is currently
				affected both by his lack of talent and his nervousness as a new
				boy at the highest level. In addition, from what I know of
				county coaching, he is unlikely to have had the opportunity to
				learn techniques that could compensate for his lack of natural
				ability. This will be addressed by the England coaching team.</p>
				<p>His batting is apparently better than he has so far
				demonstrated in the middle. Again, specialist coaching will help
				him make the most of whatever skills he has. He may become a
				reliable number nine in time.</p>
				<p>It is for his bowling that he has been selected, and it is
				his bowling that marks him out as a genuine international
				cricketer. Whatever nerves he displays in the field or with the
				bat seem to disappear when the ball is thrown to him. His
				deliveries have not turned as much as some of us hoped when his
				selection was first discussed but it is his flight, his loop,
				his control that have impressed anybody who loves spin bowling.
				He is a young man but seems to have a natural understanding of
				how to draw a batsman into an ill-advised stroke. He should have
				a long career ahead of him if the selectors recognise that his
				gifts outweigh his deficiencies.</p>
				<p>What turns me from an objective admirer into something of a
				fan is his transparent delight in his successes. English people,
				particularly journalists, have a tendency towards cynicism. To
				be cool and not overtly keen is often seen as a good quality. I
				have no idea why this is but it irritates me. Increasingly. To
				see an Englishman whose keenness is supremely overt is a welcome
				and refreshing sight. Few others in the national team
				demonstrate this with the notable exception of the
				aforementioned Kevin Pietersen, whose Englishness is adopted.
				Pietersen’s puppyish delight in simply being on the field is now
				matched by Panesar’s unselfconscious celebration of a wicket.</p>
				<p>Here’s to not being cool. Here’s to freedom of expression.</p>

