	<h2>Monty Panesar, part 2</h2>


	<p>
				Most people seemed to thing Duncan Fletcher, the England coach,
				was being a bit hard on Monty Panesar when he said before the
				third Test at Headingley: ‘<a target="_blank" href="http://content-uk.cricinfo.com/engvpak/content/story/255169.html">I
				think Monty is a very good bowler, but we have to produce 11
				players who can produce two of the departments efficiently,
				whoever is playing for England</a>‘.</p><p>In the general
				over-excited chatter about Panesar’s potential, to which I made
				<a href="montypanesar1.php">
				this</a> contribution, Fletcher’s comments were a distinct note
				of caution which drew a lot of attention because they were so
				out of step with the mood music. He was quite right to be
				realistic and it is indeed too early to start thinking of
				Panesar as a great player (great players are those for whom
				exceptions can be made). His philosophy of expecting
				international-class contributions from his players in at least
				two departments is a good one in theory and the results have
				largely backed him up. For what it’s worth I fully agree with
				him on this.</p>
				<p>The only slight issue I have is with the sophistication of
				his analysis. He is talking about contributions in two of three
				departments: bowling, batting and fielding. This is a very
				simplistic analysis of the roles that team members play in a
				Test Match side. If you consider the following roles then you
				can make a better assessment of the value of a player to the
				team:</p>
				<ul>
					<li>Opening batsman
					</li>
					<li>Middle-order strokemaker
					</li>
					<li>Lower-middle-order batsman who can make runs with the
					tail-enders
					</li>
					<li>Wicket-keeper
					</li>
					<li>Close fielder (catcher)
					</li>
					<li>Run-saving fielder
					</li>
					<li>Strike bowler
					</li>
					<li>Economical bowler
					</li>
					<li>Captain
					</li>
					<li>Morale officer
					</li>
					<li>Senior pro </li>
				</ul>
				<p>Some of these roles have more value than others. A good
				captain could probably manage without any senior pros. A team
				with four strike bowlers may not ever need an economical bowler
				to contain the runs at one end.</p>
				<p>With this analysis we can see that Panesar’s value to the
				side has more than one dimension. His excellent control means
				that he will hardly ever concede runs quickly so he is a very
				valuable economical bowler. Furthermore he can bowl many overs
				because he is not exerting himself as much as a fast bowler.
				Without a containing spinner you need two economical seam
				bowlers or a bits and pieces player who can bowl a few overs to
				give the others a rest.</p><p>
				On the other hand, here we have a spin bowler who can take
				wickets and who has already won matches on good wickets despite
				being a relative novice. It is difficult to overstate the
				importance of this - individual match winners are a rare breed;
				when they are young and improving they are a precious thing to
				be looked after with great care.</p>
				<p>This isn’t the end of Panesar’s contribution. The morale of a
				team is crucial to those 50-50 situations that turn draws into
				wins and gives people the determination to avoid defeat.
				Panesar’s effect on the team’s morale is easy to see. His
				obvious delight at just playing cricket is such a welcome
				contrast with the Old Pro atmosphere that infected the England
				teams of the Mike Atherton and Nasser Hussain eras. It’s
				difficult to escape the feeing that Panesar (and Kevin Pietersen
				for that matter) would play even if they weren’t being paid.
				They are plainly not hoping for rain so they can go down the
				bookies.</p>
				<p>As a relatively young man, Panesar might in time develop his
				game so he can contribute in other areas as well. But my
				contention is that is already doing plenty to make him an
				automatic selection on any wicket against any opposition. </p>


