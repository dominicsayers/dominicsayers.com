	<h2>Lost place names</h2>

	<p>
				English is a dynamic language and most people now seem to think
				this is	greatly to its advantage. That doesn’t mean we can’t
				occasionally mourn the passing of a part of our language.</p><p>The victims in this case
				(there are two) are place names.</p>
				<p>Chiltern Railways announcers: please note the station is
				called Princes Risborough (i.e. Prince’s Risborough), not Princess Risborough,
				tempting though it is to think of such a fairytale character. The lost possessive apostrophe is
				not something over which we should these days get too upset - it does tend to go missing in names
				with the honorable exception of underground stations like King's Cross and St. Paul's. The
				pismronunciation (as Ronnie Barker once called it) is altogether more dissatisfying.</p>
				<p>London Underground announcers: nobody really knows why the
				area is called Marylebone, but it definitely wasn’t named after anybody called
				Marley (as in Marleybone). OK, it’s an anagram of the real name, but then
				so is Be Normal Len.</p>

