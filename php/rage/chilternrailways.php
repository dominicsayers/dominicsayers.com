	<h2>Chiltern Railways, a user experience</h2>

				<p>Chiltern Railways this morning are
				struggling to run a service because of a trackside fire that is
				not their fault. In the circumstances you would be unreasonably
				optimistic to expect a smooth journey to your destination.</p>
				<p>But. You would be perfectly entitled to
				expect them to know their arse from their elbow.</p>
				<p>I started my journey in Warwick, waiting
				for a train to London Marylebone. The sole member of staff at
				this station is a notorious incompetent with a beard. The beard
				is his personal choice but his choice of career has inflicted
				misery on millions. He once refused to sell me a monthly
				travelcard because he didn’t know how to work the machine, he
				said. Today he said the next London train would be on the down
				platform for some reason and we should cross over and wait
				there.</p>
				<p>He was absolutely right and as the train
				approached the platform from the ‘wrong’ direction I prepared to
				revise my opinion of him. If it had only stopped I would have
				been singing his praises from the rooftops. But as it sailed
				past without even bothering to slow down I knew I needed to do
				something about it.</p>
				<p>Ruling out assassination of certain
				Chiltern Railways staff, I got a taxi to a real station,
				Leamington Spa. Here was a gaggle of staff in hi-viz jackets
				informing passengers about the service this morning as they
				understood it. Which amounted to “Dunno mate, just get on the
				first train through”.</p>
				<p>A train arrived. We got on. We got off
				again when the nice man in the hi-viz jacket said “Sorry mate we
				haven’t got a conductor for this one, you’ll have to wait for
				the next one”.</p>
				<p>We waited.</p>

