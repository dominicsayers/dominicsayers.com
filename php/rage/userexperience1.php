	<h2>Acer Aspire L200 Media Center, a user experience</h2>


	<p>
				WARNING: Acer Aspire L200 Media Center running Microsoft Windows
				XP Media Center Edition 2005 can’t be connected to a Sky set-top
				box</p><p>These Media Center PCs are supposed to connect to your
				aerial or set-top box and show you live TV through the PC. Why
				would you want to do that? Well because it is a computer you can
				combine this with other features in the same box: you can listen
				to music from your hard disk or CDs, watch videos from your hard
				disk or DVD player, listen to the radio and look at your family
				photographs. All this is combined into an easy-to-use interface
				for non-technical people.</p>
				<p>So it should just work, right?</p>
				<p>Not if you are connecting a Sky set-top box to your Acer
				Aspire L200 it doesn’t. To set this up you need a particular
				wire that Acer do not include with the PC. The kind of wire
				doesn’t matter: if you’re interested it’s an infra-red emitter
				that you stick over the magic eye on your Sky set-top box so the
				Media Center remote control can change the channels. This is
				important so that the PC can record a particular channel when
				you’re out or asleep.</p>
				<p>I cannot, simply cannot, believe I am the only person in the
				UK who has tried to do this. I called Acer Technical Support.
				The young man reading his script said he couldn’t help. I asked
				him to talk to his supervisor. He came back on the line. Oh yes,
				he said, you need an infra-red emitter cable. You can get them
				from our Parts Sales department on this number.</p>
				<p>Yeah right. He had successfully got rid of me and of course
				Parts Sales had never heard of this and certainly had no such
				cable in stock.</p>
				<p>But why sell the PC in the first place without a 50p cable
				that enables a very common setup? Let me be clear: it’s not just
				that I cannot change the channel with the Media Center remote,
				it’s that the Media Center setup fails if this cable isn’t
				plugged in. That’s right, Windows XP Media Center setup depends
				on this cable and Acer don’t put one in the box or even supply
				one after market.</p>
				<p>I’ve ordered one from elsewhere. I have the technical
				know-how to fix this. But this is a consumer device. My user
				experience has been very poor indeed. I recommend you do not
				purchase an Acer Aspire L200 Media Center.</p>

