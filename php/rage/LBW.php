	<h2>Law 36 (Leg before wicket)</h2>

	<p>
				If you have no interest in the minutiae of the laws of cricket
				then you can skip this post with my blessing. Cricket nerds read
				on.</p><p>I believe, but I cannot now prove, that the LBW law was
				different in the olden days when I played the game regularly.
				The current set of laws dates from 2000 with a number of changes
				in 2003. I finished playing the game regularly about then.</p>
				<p>Back in the day it was generally believed by club cricketers
				that if a straight ball hit the batsman’s pad on the full then
				he was out LBW <b>only</b> if the ball would also have hit the
				stumps on the full. In other words, if the ball would have
				pitched in the blockhole then the batsman was Not Out on the
				grounds that it was impossible to predict what the ball would
				have done when it pitched in that rough area of the wicket.</p>
				<p>Now it seems the interpretation of the law, and in fact the
				law itself, is a little different. Here is
				<a target="_blank" href="http://www.lords.org/laws-and-spirit/laws-of-cricket/laws/law-36-leg-before-wicket,62,AR.html">
				Law 36.2(b)</a> as it currently stands: “it is to be assumed
				that the path of the ball before interception would have
				continued after interception, irrespective of whether the ball
				might have pitched subsequently or not.”</p>
				<p>And the
				<a target="_blank" href="http://www.lords.org/data/files/law_36_qanda-9684.pdf">
				guide to its interpretation</a> says “The Law does not require
				any judgment as to what might have happened if the ball had
				pitched after any interception, so the vagaries of the pitch are
				eliminated from the equation”.</p>
				<p>Heavens to betsy! The batsman is out whatever the ball might
				have done when it pitched. Whatever happened to giving the
				batsman the benefit of the doubt? In fact there is no reference
				to this concept at all in the Laws of Cricket (2000 Code),
				something that I found quite startling as will many other
				people, I suspect. The only time when the batsman is given the
				benefit of doubt is when the umpire has referred a decision to
				the other umpire because he believes the other umpire was in a
				better position to adjudicate. If they cannot agree on a
				decision then, and only then, is the batsman given Not Out due
				to doubt in the umpire’s mind.</p>
				<p>There is very little chance I will find myself umpiring a
				serious game of cricket any time soon, but I am still having a
				Trevor Bailey moment over this. How many of my other dearly-held
				beliefs about the laws are also wrong?</p>

