	<h2>Vendor lock-in, part 1</h2>
	<p>
				I had a conversation with
				<a target="_blank" href="http://confusedofcalcutta.com">
				JP Rangaswami</a> and a
				<a target="_blank" href="http://www2.severntrent.com/aboutus/boardofdirectors.php">
				captain of industry</a> a few weeks ago and asked them the above
				question. My implication was that it is a good strategy because
				it fosters trust. It shows that you are confident that the
				service you provide is competitive. Attempting to lock your
				customers into your service by making it difficult for them to
				switch just shows that you are afraid of the competition.</p><p>I
				was hoping for a perspective from the real world - I have never
				had a customer base that I was afraid of losing. I imagine that
				a public company that adopted this strategy might face some
				tough questions from shareholders.</p>
				<p>The UK government has legislated to enforce this ability to
				switch suppliers in industries where it is guilty of creating a
				private monopoly (i.e. the utilities that my bank helped it sell
				off in the 1980s). It is a retrospective attempt to correct the
				skewed market that they created. There is no indication that any
				of these companies would have adopted this policy themselves.</p>
				<p>I know of only one example where a company has publicly said
				it would help its customers move their business away from them
				and that is hedged by the proviso that they will only help them
				move to a competitor with whom they have a reciprocal agreement.
				The company is <a target="_blank" href="http://www.flickr.com">
				Flickr</a>, part of the
				<a target="_blank" href="http://www.yahoo.com">Yahoo</a> group.
				Here is
				<a target="_blank" href="http://www.flickr.com/people/stewart/">
				Stewart Butterfield</a>&#39;s
				<a target="_blank" href="http://www.flickr.com/groups/central/discuss/72157594165399644/#comment72157594167782546">
				statement</a>, as mentioned in
				<a target="_blank" href="http://yro.slashdot.org/article.pl?sid=06/06/18/203209&amp;from=rss">
				Slashdot</a> and
				<a target="_blank" href="http://www.boingboing.net/2006/06/18/flickr_well_give_ful.html">
				Boing Boing</a>.</p>
				<p>The captain of industry I mentioned above said he believed
				the only worthwhile strategy was to pursue the highest quality
				service at the lowest possible price. A somewhat stock answer I&#39;m
				guessing, but I believe he meant that a deliberate lock-in
				wasn&#39;t a valuable part of a high-level strategy. JP said
				something gnomic so I won&#39;t put words in his mouth. I would be
				interested in what he and
				<a target="_blank" href="http://parkparadigm.com/">Sean Park</a>
				had to say on the subject.
	</p>

